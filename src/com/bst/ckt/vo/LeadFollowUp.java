package com.bst.ckt.vo;

public class LeadFollowUp {

	private int followUpId;
	private String followUpDate;
	private String followUpBy;
	private String followUpStatus;
	private String followUpComment;
	/**
	 * @return the followUpId
	 */
	public int getFollowUpId() {
		return followUpId;
	}
	/**
	 * @param followUpId the followUpId to set
	 */
	public void setFollowUpId(int followUpId) {
		this.followUpId = followUpId;
	}
	/**
	 * @return the followUpDate
	 */
	public String getFollowUpDate() {
		return followUpDate;
	}
	/**
	 * @param followUpDate the followUpDate to set
	 */
	public void setFollowUpDate(String followUpDate) {
		this.followUpDate = followUpDate;
	}
	/**
	 * @return the followUpBy
	 */
	public String getFollowUpBy() {
		return followUpBy;
	}
	/**
	 * @param followUpBy the followUpBy to set
	 */
	public void setFollowUpBy(String followUpBy) {
		this.followUpBy = followUpBy;
	}
	/**
	 * @return the followUpStatus
	 */
	public String getFollowUpStatus() {
		return followUpStatus;
	}
	/**
	 * @param followUpStatus the followUpStatus to set
	 */
	public void setFollowUpStatus(String followUpStatus) {
		this.followUpStatus = followUpStatus;
	}
	/**
	 * @return the followUpComment
	 */
	public String getFollowUpComment() {
		return followUpComment;
	}
	/**
	 * @param followUpComment the followUpComment to set
	 */
	public void setFollowUpComment(String followUpComment) {
		this.followUpComment = followUpComment;
	}
	
	
}
